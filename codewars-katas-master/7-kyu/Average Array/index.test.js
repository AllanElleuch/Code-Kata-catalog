const avgArray = require('.')

test('Test 1', () => {
  expect(avgArray([[1, 2, 3, 4], [5, 6, 7, 8]])).toEqual([3, 4, 5, 6])
})

test('Test 2', () => {
  expect(avgArray([[2, 3, 9, 10, 7], [12, 6, 89, 45, 3], [9, 12, 56, 10, 34], [67, 23, 1, 88, 34]])).toEqual([
    22.5,
    11,
    38.75,
    38.25,
    19.5,
  ])
})

test('Test 3', () => {
  expect(avgArray([[1.2, 8.521, 0.4, 3.14, 1.9], [2, 4.5, 3.75, 0.987, 1]])).toEqual([
    1.6,
    6.5105,
    2.075,
    2.0635,
    1.45,
  ])
})
